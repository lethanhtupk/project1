package BEAN;

public class Examination {
	
	private int  examinationid;
	private String examinationname ;
	private String examinationimage;
	
	private int checkexamination;
	
	public int getExaminationid() {
		return examinationid;
	}
	public void setExaminationid(int examinationid) {
		this.examinationid = examinationid;
	}
	
	
	public String getExaminationname() {
		return examinationname;
	}
	public void setExaminationname(String examinationname) {
		this.examinationname = examinationname;
	}
	
	
	public String getExaminationimage() {
		return examinationimage;
	}
	public void setExaminationimage(String examinationimage) {
		this.examinationimage = examinationimage;
	}
	
	
	public int getCheckexamination() {
		return checkexamination;
	}
	public void setCheckexamination(int checkexamination) {
		this.checkexamination = checkexamination;
	}

	
	
}
